    private void btnGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar1ActionPerformed

        if (txtDNI2.getText().isEmpty() || txtNombreC2.getText().isEmpty() || txtTelefono.getText().isEmpty() || txtCiudad.getText().isEmpty() || jComboBoxC.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Por favor, completa todos los campos.");
            return;
        }

        String dniText = txtDNI2.getText();

        dniText = dniText.trim().replaceAll("[^0-9]", "");

        if (!dniText.matches("[0-9]+") || dniText.length() != 8) {
            JOptionPane.showMessageDialog(null, "El DNI debe contener exactamente 8 números válidos.");
            return;
        }

        String telefonoText = txtTelefono.getText();
        telefonoText = telefonoText.trim().replaceAll("[^0-9]", "");
        if (!telefonoText.matches("[0-9]+")) {
            JOptionPane.showMessageDialog(null, "El teléfono debe contener solo números.");
            return;
        }

        c.setDNI(Integer.parseInt(dniText));
        c.setNombre(txtNombreC2.getText());
        c.setTelefono(Integer.parseInt(telefonoText));
        c.setCiudad(txtCiudad.getText());
        c.setTipo(jComboBoxC.getSelectedItem().toString());
        cd.RegistroClient(c);
        BorrarTabla();
        LimpiarCasillasC();
        ListaClientes();
        JOptionPane.showMessageDialog(null, "Cliente Guardado");
    }//GEN-LAST:event_btnGuardar1ActionPerformed

