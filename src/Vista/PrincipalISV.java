    public void ListaClientes() {
        List<Cliente> ListarCl = cd.Listar();
        model = (DefaultTableModel) TablaClient.getModel();
        Object[] ob = new Object[5];
        for (int i = 0; i < ListarCl.size(); i++) {
            ob[0] = ListarCl.get(i).getDNI();
            ob[1] = ListarCl.get(i).getNombre();
            ob[2] = ListarCl.get(i).getTelefono();
            ob[3] = ListarCl.get(i).getCiudad();
            ob[4] = ListarCl.get(i).getTipo();
            model.addRow(ob);
        }
        TablaClient.setModel(model);
    }

    public void BorrarTabla() {
        for (int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(i);
            i = i - 1;
        }
    }

