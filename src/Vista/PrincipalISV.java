    private void txtDNIKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDNIKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!"".equals(txtDNI.getText())) {
                int DNI = Integer.parseInt(txtDNI.getText());
                c = cd.BuscarC(DNI);
                if (c.getNombre() != null) {
                    txtNombreC1.setText("" + c.getNombre());
                    txtTipo.setText("" + c.getTipo());
                    txtTelefonoV.setText("" + c.getTelefono());
                    txtCiudadV.setText("" + c.getCiudad());
                } else {
                    txtDNI.setText("");
                    JOptionPane.showMessageDialog(null, "Cliente Inexistente");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingresa el DNI del cliente");
                txtDNI.requestFocus();
            }
        }
    }//GEN-LAST:event_txtDNIKeyPressed

