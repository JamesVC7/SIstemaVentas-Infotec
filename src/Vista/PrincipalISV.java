    private void txtCantidad1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidad1KeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!"".equals(txtCantidad1.getText())) {
                String id = txtIDProd1.getText();
                String producto = txtProducto.getText();
                String categoria = txtCategoria1.getText();
                int cant = Integer.parseInt(txtCantidad1.getText());
                double precio = Double.parseDouble(txtPrecio1.getText());
                double total = cant * precio;
                int stock = Integer.parseInt(txtStock.getText());
                if (stock >= cant) {
                    item = item + 1;
                    DefaultTableModel tb = (DefaultTableModel) TablaNV.getModel();
                    ArrayList listV = new ArrayList();
                    listV.add(item);
                    listV.add(id);
                    listV.add(producto);
                    listV.add(categoria);
                    listV.add(cant);
                    listV.add(precio);
                    listV.add(total);
                    Object[] ob = new Object[6];
                    ob[0] = listV.get(1);
                    ob[1] = listV.get(2);
                    ob[2] = listV.get(3);
                    ob[3] = listV.get(4);
                    ob[4] = listV.get(5);
                    ob[5] = listV.get(6);
                    tb.addRow(ob);
                    TablaNV.setModel(tb);
                    TotalV();
                    LimpiarCasillasV();
                    txtIDProd1.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "Stock insuficiente");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingresar cantidad");
            }
        }
    }//GEN-LAST:event_txtCantidad1KeyPressed

